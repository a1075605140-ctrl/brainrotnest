export function translateToBrainrot(text: string, intensity: TranslationIntensity): string {
  if (!text.trim()) return "";

  const words = text.split(/(\s+|[.,!?;:]+)/);
  const result: string[] = [];

  let wordCount = 0;
  const replacementRate = intensity === "mild" ? 0.3 : intensity === "medium" ? 0.55 : 0.8;

  for (const token of words) {
    const lowerToken = token.toLowerCase().replace(/[^a-z]/g, "");

    if (brainrotVocabulary[lowerToken] && Math.random() < replacementRate) {
      const options = brainrotVocabulary[lowerToken];
      const replacement = options[Math.floor(Math.random() * options.length)];

      if (intensity === "maximum") {
        result.push(replacement.toUpperCase());
      } else if (intensity === "medium") {
        result.push(replacement);
      } else {
        result.push(replacement);
      }
      wordCount++;
    } else {
      result.push(token);
    }
  }

  let translated = result.join("");

  if (intensity === "maximum") {
    const additions = intensityPhraseAdditions.maximum;
    const randomAddition = additions[Math.floor(Math.random() * additions.length)];
    translated += randomAddition;
  } else if (intensity === "medium" && Math.random() > 0.5) {
    const additions = intensityPhraseAdditions.medium;
    const randomAddition = additions[Math.floor(Math.random() * additions.length)];
    translated += randomAddition;
  }

  if (intensity !== "mild" && Math.random() > 0.6) {
    const suffix = brainrotSuffixes[Math.floor(Math.random() * brainrotSuffixes.length)];
    translated += " вҖ” " + suffix;
  }

  return translated;
}

export function translateFullText(text: string, intensity: TranslationIntensity): string {
  const sentences = text.split(/(?<=[.!?])\s+/);
  return sentences
    .map((sentence) => translateToBrainrot(sentence, intensity))
    .join(" ");
}
